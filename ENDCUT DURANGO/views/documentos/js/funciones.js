function handlePdfUpload(input) {
    const file = input.files[0];
  
    if (file && file.type === "application/pdf") {
      // Aquí puedes agregar la lógica para cargar el archivo PDF
      // Por ejemplo, enviar una solicitud AJAX para subir el archivo a un servidor
  
      // Después de cargar el PDF, actualiza el estado
      document.getElementById("documentStatus").textContent =
        "PDF cargado exitosamente";
    } else {
      // Si el archivo no es un PDF, muestra un mensaje de error
      document.getElementById("documentStatus").textContent =
        "Selecciona un archivo PDF válido";
    }
  
    // Restablece el valor del input para que el usuario pueda cargar otro archivo si es necesario
    input.value = "";
  }
  
